function genDrill(chars: string, length: number = 60) {
  const charArray = chars.split('');
  let result: string[] = [];
  
  // ‡πÉ‡∏™‡πà‡πÉ‡∏´‡πâ‡∏Ñ‡∏£‡∏ö‡∏ó‡∏∏‡∏Å‡∏ï‡∏±‡∏ß‡∏Å‡πà‡∏≠‡∏ô 1 ‡∏£‡∏≠‡∏ö
  result.push(...charArray);

  // ‡∏™‡∏∏‡πà‡∏°‡πÉ‡∏™‡πà‡πÄ‡∏û‡∏¥‡πà‡∏°‡∏à‡∏ô‡∏Ñ‡∏£‡∏ö‡∏Ñ‡∏ß‡∏≤‡∏°‡∏¢‡∏≤‡∏ß
  for (let i = 0; i < length - charArray.length; i++) {
    const randomIndex = Math.floor(Math.random() * charArray.length);
    result.push(charArray[randomIndex]);
  }

  // Shuffle (Fisher-Yates)
  for (let i = result.length - 1; i > 0; i--) {
    const j = Math.floor(Math.random() * (i + 1));
    [result[i], result[j]] = [result[j], result[i]];
  }

  return result.join(''); 
}

